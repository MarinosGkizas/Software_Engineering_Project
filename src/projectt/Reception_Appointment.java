    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         

        if(jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this,("Επιλέξτε ημερομηνία!"));
            
        }
        else {  
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/project", "root", "");
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(jDateChooser1.getDate());
                String sql = "SELECT CONCAT(first_name,' ',last_name) AS Ονοματεπώνυμο, TIME(app_date) AS Ώρα FROM appointment INNER JOIN user on patient_id=user_id WHERE DATE(app_date)='"+date+"'";
                PreparedStatement pst = con.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();

                if(!rs.isBeforeFirst()) {
                    //DateChooser.setCalendar(null);
                    jTable1.setModel(new DefaultTableModel(null,new String[]{"Ονοματεπώνυμο", "Ημερομηνία"}));
                    JOptionPane.showMessageDialog(this, "Δεν υπάρχουν ραντεβού για αυτή την ημερομηνία.");
                }
                else {
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                }

            } catch(Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }
        }    
    }
